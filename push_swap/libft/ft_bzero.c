#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*p;
	int		x;

	x = n;
	p = (char *)s;
	while (n > 0)
	{
		p[x - 1] = '\0';
		n--;
		x--;
	}
}
